import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Mark on a class or method so it enables FastJSON's circular reference detect.
 *
 * If it is marked on a class, then all request handler methods in this class is
 * subject to circular reference detect.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.TYPE, ElementType.METHOD})
public @interface EnableCircularReferenceDetect {
}
